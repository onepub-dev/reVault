local revault = require('revault_api')
local api = revault.Vault.new()
local separator = package.config:sub(1, 1)
local windows = separator == '\\'
local temporary = os.getenv('TMPDIR') or os.getenv('TEMP') or (windows and '.' or '/tmp')

local function pass(symbol, assertions)
  print(string.format('PASS\tlua\t%s\t%d', symbol, assertions or 1))
end
local function check(value, message) if not value then error(message, 2) end end
local function quote(path) return '"' .. path:gsub('"', '\\"') .. '"' end
local function mkdir(path)
  local command = windows and ('mkdir ' .. quote(path) .. ' 2>NUL') or ('mkdir -p ' .. quote(path))
  local ok = os.execute(command); assert(ok == true or ok == 0)
end
local function remove(path)
  os.execute(windows and ('rmdir /s /q ' .. quote(path) .. ' 2>NUL') or ('rm -rf ' .. quote(path)))
end
local function pause()
  local ffi = require('ffi')
  if windows then ffi.cdef('void Sleep(unsigned long);'); ffi.C.Sleep(50)
  else ffi.cdef('int usleep(unsigned int);'); ffi.C.usleep(50000) end
end
local function write(path, bytes)
  local file = assert(io.open(path, 'wb')); assert(file:write(bytes)); file:close()
end
local function read(path)
  local file = assert(io.open(path, 'rb')); local value = assert(file:read('*a')); file:close(); return value
end
local function bytes(first, last)
  local value = {}; for index = first, last do value[#value + 1] = string.char(index) end
  return table.concat(value)
end
local function artifact_root()
  local path = (os.getenv('REVAULT_E2E_ARTIFACT_DIR') or '/tmp/revault-e2e-artifacts') .. '/lua'
  mkdir(path); return path
end
local function fields()
  return revault.Models.FormFieldList.new({ values = {
    revault.Models.FormField.new({ id = 'username', label = 'Username', kind = 'text', required = true })
  }}):encode()
end
local function moves(source, destination)
  return revault.Models.PathMoveList.new({ values = {
    revault.Models.PathMove.new({ source = source, destination = destination })
  }}):encode()
end

