local function key_lifecycle()
  local content = bytes(0, 31)
  local contact = api:key_contact_generate(); pass('key_contact_generate')
  local private_key = contact:private(); pass('key_contact_private', 2)
  local copy = api:key_contact_from_private(private_key); pass('key_contact_from_private')
  local public_bytes = contact:public(); pass('key_contact_public', 2)
  local public_key = api:key_contact_public_from_bytes(public_bytes); pass('key_contact_public_from_bytes')
  local wrapped = public_key:encrypt(content); pass('key_contact_encrypt')
  check(copy:decrypt(wrapped) == content, 'decrypt'); pass('key_contact_decrypt', 3)
  check(#wrapped:public() > 0 and #wrapped:ciphertext() > 0 and #wrapped:encrypted() > 0, 'wrapped')
  pass('key_contact_wrapped_public', 2); pass('key_contact_wrapped_ciphertext', 2); pass('key_contact_wrapped_encrypted', 2)
  wrapped:free(); pass('key_contact_wrapped_free')
  local imported_private = api:vault_key_import_private(api:vault_key_export_private(contact, 'raw-hex'))
  pass('vault_key_export_private', 2); pass('vault_key_import_private')
  local imported_public = api:vault_key_import_public(api:vault_key_export_public(public_key, 'lockbox-pem'))
  pass('vault_key_export_public', 2); pass('vault_key_import_public')
  local fingerprint = api:vault_key_fingerprint(imported_public); pass('vault_key_fingerprint', 2)
  local hex = api:vault_key_format_hex(fingerprint); check(api:vault_key_decode_hex(hex) == fingerprint, 'hex')
  pass('vault_key_format_hex', 2); pass('vault_key_decode_hex', 2)
  local short = fingerprint:sub(1, 12); local code = api:vault_key_format_crockford(short)
  api:vault_key_format_crockford_reading(code); check(api:vault_key_decode_crockford(code) == short, 'crockford')
  pass('vault_key_format_crockford', 2); pass('vault_key_format_crockford_reading', 2); pass('vault_key_decode_crockford', 2)
  imported_public:public_free(); public_key:public_free(); pass('key_contact_public_free', 2)
  imported_private:free(); copy:free(); contact:free(); pass('key_contact_free', 3)
  local plain = api:vault_key_hex_encode(content); check(api:vault_key_hex_decode(plain) == content, 'plain hex')
  pass('vault_key_hex_encode', 2); pass('vault_key_hex_decode', 2)
  local signing = api:key_signing_generate(); pass('key_signing_generate')
  local signing_private, signing_public = signing:private(), signing:public()
  pass('key_signing_private', 2); pass('key_signing_public', 2)
  api:key_signing_from_private(signing_private):free(); pass('key_signing_from_private')
  api:key_signing_public_from_bytes(signing_public):public_free()
  pass('key_signing_public_from_bytes'); pass('key_signing_public_free', 2)
  signing:free(); pass('key_signing_free', 3)
end

