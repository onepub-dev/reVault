func pass(symbol string, assertions ...int) {
	count := 1
	if len(assertions) > 0 {
		count = assertions[0]
	}
	fmt.Printf("PASS\tgo\t%s\t%d\n", symbol, count)
}
func check(condition bool, message string) {
	if !condition {
		panic(message)
	}
}
func must(err error) {
	if err != nil {
		panic(err)
	}
}
func mustValue[T any](value T, err error) T { must(err); return value }
func artifactRoot() string {
	root := os.Getenv("REVAULT_E2E_ARTIFACT_DIR")
	if root == "" {
		root = "/tmp/revault-e2e-artifacts"
	}
	root = filepath.Join(root, "go")
	must(os.MkdirAll(root, 0700))
	return root
}

