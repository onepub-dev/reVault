func interopOpen(producer string) {
	base := os.Getenv("REVAULT_E2E_ARTIFACT_DIR")
	if base == "" {
		base = "/tmp/revault-e2e-artifacts"
	}
	archive := mustValue(os.ReadFile(filepath.Join(base, producer, "archive.lbox")))
	box := mustValue(revault.Open(archive, bytes.Repeat([]byte{'K'}, 32)))
	check(bytes.Equal(mustValue(box.GetFile("/renamed.txt")), []byte("replacement payload")), "foreign archive")
	box.Close()
	vault := mustValue(revault.OpenVaultDirectory(filepath.Join(base, producer, "vault"), []byte("new vault password")))
	check(vault.StructureVersion() > 0, "foreign vault")
	vault.Close()
	fmt.Printf("INTEROP\tgo\t%s\tarchive\t3\n", producer)
	fmt.Printf("INTEROP\tgo\t%s\tvault\t2\n", producer)
}

