func agentAndLocalVault() {
	agentDir := mustValue(os.MkdirTemp("", "revault-go-agent-"))
	agentVaultDir := mustValue(os.MkdirTemp("", "revault-go-agent-vault-"))
	must(os.Setenv("LOCKBOX_SESSION_AGENT_DIR", agentDir))
	must(os.Setenv("LOCKBOX_VAULT_DIR", agentVaultDir))
	must(os.Setenv("LOCKBOX_VAULT_PASSWORD", "agent vault password"))
	vault := mustValue(revault.ReplaceDefaultVaultDirectory([]byte("agent vault password")))
	profile := mustValue(revault.GenerateContactKeyPair())
	must(vault.StorePrivateKey("default", profile))
	profile.Close()
	vault.Close()
	must(revault.ForgetAllAgentSecrets())
	pass("vault_forget_all")
	child := exec.Command(os.Args[0], "--serve-agent")
	child.Stdout = os.Stderr
	child.Stderr = os.Stderr
	must(child.Start())
	running := false
	for attempt := 0; attempt < 200; attempt++ {
		if revault.AgentIsRunning() {
			running = true
			break
		}
		time.Sleep(50 * time.Millisecond)
	}
	check(running, "agent start")
	pass("vault_agent_serve")
	pass("vault_is_running")
	must(revault.StartAgent())
	pass("vault_agent_start")
	must(revault.VerifyAgentTransport())
	pass("vault_agent_verify_transport")
	id := make([]byte, 16)
	key := make([]byte, 32)
	for index := range id {
		id[index] = byte(0xc0 + index)
	}
	for index := range key {
		key[index] = byte(0x20 + index)
	}
	must(revault.PutAgentKey(id, key))
	check(bytes.Equal(mustValue(revault.GetAgentKey(id)), key), "agent key")
	check(len(mustValue(revault.ListAgentKeys())) > 0, "agent list")
	pass("vault_agent_put")
	pass("vault_agent_get", 3)
	pass("vault_agent_list")
	must(revault.PutAgentVaultUnlockKey("vault-id", key, 120))
	check(bytes.Equal(mustValue(revault.GetAgentVaultUnlockKey("vault-id")), key), "vault key")
	pass("vault_agent_put_vault_unlock_key")
	pass("vault_agent_get_vault_unlock_key", 3)
	owner := mustValue(revault.GenerateSigningKeyPair())
	defer owner.Close()
	must(revault.PutAgentOwnerSigningKey("vault-id", "alice", owner, 120))
	loadedOwner := mustValue(revault.GetAgentOwnerSigningKey("vault-id", "alice"))
	check(len(mustValue(loadedOwner.PublicBytes())) > 0, "owner key")
	loadedOwner.Close()
	pass("vault_agent_put_owner_signing_key")
	pass("vault_agent_get_owner_signing_key")
	activity := mustValue(revault.BeginAgentActivity("open"))
	pass("vault_agent_begin_activity")
	activity.Close()
	pass("vault_agent_end_activity")
	mustValue(revault.AgentSleepSupport())
	pass("vault_agent_sleep_support")
	check(mustValue(revault.AgentLogPath()) != "", "log path")
	check(mustValue(revault.AgentLogDestination()) != "", "log destination")
	pass("vault_agent_log_path", 2)
	pass("vault_agent_log_destination", 2)
	local := mustValue(revault.OpenLocalVault())
	pass("vault_local")
	localRoot := mustValue(os.MkdirTemp("", "revault-go-local-"))
	payload := []byte("local vault data")
	passwordPath := filepath.Join(localRoot, "password.lbox")
	passwordBox := mustValue(local.CreateWithPassword(passwordPath, []byte("local password")))
	must(passwordBox.AddFile("/data.txt", payload, false))
	must(passwordBox.Commit())
	passwordBox.Close()
	pass("vault_create_lockbox_password", 3)
	must(local.CachePassword(passwordPath, []byte("local password"), 120))
	pass("vault_cache_lockbox_password")
	passwordOpen := mustValue(local.OpenWithPassword(passwordPath, []byte("local password")))
	check(bytes.Equal(mustValue(passwordOpen.GetFile("/data.txt")), payload), "password open")
	passwordOpen.Close()
	pass("vault_open_lockbox_password", 3)
	must(local.CloseLockbox(passwordPath))
	pass("vault_close_lockbox")
	contentPath := filepath.Join(localRoot, "content.lbox")
	contentBox := mustValue(local.CreateWithContentKey(contentPath, key, owner))
	must(contentBox.AddFile("/data.txt", payload, false))
	must(contentBox.Commit())
	contentBox.Close()
	pass("vault_create_lockbox_content_key", 3)
	contentOpen := mustValue(local.OpenWithContentKey(contentPath, key, owner))
	check(bytes.Equal(mustValue(contentOpen.GetFile("/data.txt")), payload), "content open")
	contentOpen.Close()
	pass("vault_open_lockbox_content_key", 3)
	contact := mustValue(revault.GenerateContactKeyPair())
	defer contact.Close()
	public := mustValue(contact.PublicKey())
	defer public.Close()
	contactPath := filepath.Join(localRoot, "contact.lbox")
	contactBox := mustValue(local.CreateForContact(contactPath, public, "recipient", owner))
	must(contactBox.AddFile("/data.txt", payload, false))
	must(contactBox.Commit())
	contactBox.Close()
	pass("vault_create_lockbox_contact", 3)
	must(local.CloseAll())
	pass("vault_close_all")
	local.Close()
	pass("vault_free")
	must(revault.ForgetAgentOwnerSigningKey("vault-id", "alice"))
	must(revault.ForgetAgentVaultUnlockKey("vault-id"))
	must(revault.ForgetAgentKey(id))
	pass("vault_agent_forget_owner_signing_key")
	pass("vault_agent_forget_vault_unlock_key")
	pass("vault_agent_forget")
	must(revault.StopAgent())
	pass("vault_agent_stop")
	must(child.Wait())
}

