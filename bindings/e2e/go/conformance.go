func keyLifecycle() {
	content := make([]byte, 32)
	for index := range content {
		content[index] = byte(index)
	}
	contact := mustValue(revault.GenerateContactKeyPair())
	defer contact.Close()
	pass("key_contact_generate")
	private := mustValue(contact.PrivateRecord())
	check(len(private) > 32, "private record")
	pass("key_contact_private", 2)
	contactCopy := mustValue(revault.ContactKeyPairFromPrivate(private))
	defer contactCopy.Close()
	pass("key_contact_from_private")
	publicBytes := mustValue(contact.PublicBytes())
	check(len(publicBytes) > 0, "public record")
	pass("key_contact_public", 2)
	public := mustValue(revault.NewContactPublicKey(publicBytes))
	defer public.Close()
	pass("key_contact_public_from_bytes")
	wrapped := mustValue(public.Encrypt(content))
	defer wrapped.Close()
	pass("key_contact_encrypt")
	check(bytes.Equal(mustValue(contactCopy.Decrypt(wrapped)), content), "decrypt")
	pass("key_contact_decrypt", 3)
	check(len(mustValue(wrapped.PublicBytes())) > 0, "wrapped public")
	check(len(mustValue(wrapped.Ciphertext())) > 0, "wrapped cipher")
	check(len(mustValue(wrapped.EncryptedBytes())) > 0, "wrapped encrypted")
	pass("key_contact_wrapped_public", 2)
	pass("key_contact_wrapped_ciphertext", 2)
	pass("key_contact_wrapped_encrypted", 2)
	exportedPrivate := mustValue(contact.Export("raw-hex"))
	importedPrivate := mustValue(revault.ImportContactKeyPair(exportedPrivate))
	defer importedPrivate.Close()
	pass("vault_key_export_private", 2)
	pass("vault_key_import_private")
	exportedPublic := mustValue(public.Export("lockbox-pem"))
	importedPublic := mustValue(revault.ImportContactPublicKey(exportedPublic))
	defer importedPublic.Close()
	pass("vault_key_export_public", 2)
	pass("vault_key_import_public")
	fingerprint := mustValue(importedPublic.Fingerprint())
	check(len(fingerprint) >= 12, "fingerprint")
	pass("vault_key_fingerprint", 2)
	hex := mustValue(revault.FormatKeyHex(fingerprint))
	check(bytes.Equal(mustValue(revault.DecodeKeyHex(hex)), fingerprint), "hex")
	pass("vault_key_format_hex", 2)
	pass("vault_key_decode_hex", 2)
	code := mustValue(revault.FormatKeyCrockford(fingerprint[:12]))
	check(mustValue(revault.FormatKeyCrockfordReading(code)) != "", "reading")
	check(bytes.Equal(mustValue(revault.DecodeKeyCrockford(code)), fingerprint[:12]), "crockford")
	pass("vault_key_format_crockford", 2)
	pass("vault_key_format_crockford_reading", 2)
	pass("vault_key_decode_crockford", 2)
	plainHex := mustValue(revault.HexEncode(content))
	check(bytes.Equal(mustValue(revault.HexDecode(plainHex)), content), "plain hex")
	pass("vault_key_hex_encode", 2)
	pass("vault_key_hex_decode", 2)
	signing := mustValue(revault.GenerateSigningKeyPair())
	defer signing.Close()
	pass("key_signing_generate")
	signingPrivate := mustValue(signing.PrivateRecord())
	signingPublic := mustValue(signing.PublicBytes())
	check(len(signingPrivate) > 0 && len(signingPublic) > 0, "signing records")
	pass("key_signing_private", 2)
	pass("key_signing_public", 2)
	signingCopy := mustValue(revault.SigningKeyPairFromPrivate(signingPrivate))
	defer signingCopy.Close()
	pass("key_signing_from_private")
	signingPublicKey := mustValue(revault.NewSigningPublicKey(signingPublic))
	defer signingPublicKey.Close()
	pass("key_signing_public_from_bytes")
	pass("key_contact_wrapped_free")
	pass("key_contact_public_free", 2)
	pass("key_contact_free", 3)
	pass("key_signing_public_free", 2)
	pass("key_signing_free", 3)
}

